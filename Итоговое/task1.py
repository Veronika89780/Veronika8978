def fib(n: int):
    """
    Генерирует числа последовательности Фибоначчи от F0 до F_n (включительно).

    Параметры:
        n (int): индекс числа Фибоначчи, на котором остановиться (n >= 0).
                 Индексация: F0 = 0, F1 = 1, F2 = 1, ...

    Yields:
        int: очередное число Фибоначчи.

    Примеры:
        list(fib(0)) -> [0]
        list(fib(1)) -> [0, 1]
        list(fib(7)) -> [0, 1, 1, 2, 3, 5, 8, 13]
    """
    if not isinstance(n, int):
        raise TypeError("n должен быть целым числом")
    if n < 0:
        raise ValueError("n должен быть >= 0")

    a, b = 0, 1
    for _ in range(n + 1):  # включаем F_n
        yield a
        a, b = b, a + b

print(list(fib(7)))  # [0, 1, 1, 2, 3, 5, 8, 13]
